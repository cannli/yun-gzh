<template>
    <div>
        <!-- <div class="mask" @click="_hideLinkage"></div> -->
        <!-- <div> -->
        <div class="picker-toolbar" style="bottom:180px;">
            <span class="mint-datetime-action mint-datetime-cancel" @click="cancel">取消</span>
            <span class="mint-datetime-action mint-datetime-confirm" @click="confirm">确定</span>
        </div>
        <div class="clearfix">
            <div class="province pickerWrapper">
                <mt-picker :slots="provinces" @change="onProvinceChange" value-key="name"></mt-picker>
            </div>
            <div class="city pickerWrapper">
                <mt-picker :slots="citys" @change="onCityChange" value-key="name"></mt-picker>
            </div>
            <div class="area pickerWrapper">
                <mt-picker :slots="areas" @change="onAreaChange" value-key="name"></mt-picker>
            </div>
        </div>
        <!-- </div> -->
    </div>
</template>

<script>
import areaData from '../../static/js/pcas-code.json' //引入cityData数据
export default {
  computed: {
    result() {
      console.log(this.province.name + this.city.name + this.area.name)
      return {
        name: this.province.name + this.city.name + this.area.name,
        id: this.province.id + ',' + this.city.id + ',' + this.area.id
      }
    }
  },
  data() {
    return {
      areaData: '',
      province: {
        name: areaData[0].name,
        id: areaData[0].code
      },
      maskFlag: false,
      city: {
        name: areaData[0].children[0].name,
        id: areaData[0].children[0].code
      },
      area: {
        name: areaData[0].children[0].children[0].name,
        id: areaData[0].children[0].children[0].code
      },
      flag: 0, //最开始省市区那三个picker会初始化调用change事件，但是此时没有省市区数据，因此会报错，
      //所以以这个标识符来控制当时第一次初始化时调用change事件时直接return
      provinces: [
        {
          flex: 1,
          values: this._getProvince(),
          textAlign: 'center'
        }
        // {
        //   divider: true,
        //   content: "1-",
        //   className: "right"
        // }
      ],
      citys: [
        {
          flex: 1,
          values: this._getCity('北京市'),
          textAlign: 'center'
        }
        // {
        //   divider: true,
        //   content: "1-",
        //   className: "slot2"
        // }
      ],
      areas: [
        {
          flex: 1,
          values: this._getArea('北京市', '市辖区'),
          className: 'slot1',
          textAlign: 'center'
        }
      ]
    }
  },
  mounted() {
    this.areaData = areaData
  },
  methods: {
    onProvinceChange(picker, values) {
      if (this.flag === 0) {
        return
      }
      let provinceIndex = picker.getSlotValue(0)
      this.province = provinceIndex
      let city = this._getCity(provinceIndex.name)
      this.citys[0].values = city
      this.city = city[0]

      let area = this._getArea(provinceIndex.name, city[0].name)
      this.areas[0].values = area
      this.area = area[0]
    },
    onCityChange(picker, values) {
      if (this.flag === 0) {
        return
      }
      let cityIndex = picker.getSlotValue(0)
      this.city = cityIndex
      let provinceIndex = this.province
      let area = this._getArea(provinceIndex.name, cityIndex.name)
      this.areas[0].values = area
      this.area = area[0]
    },
    onAreaChange(picker, values) {
      if (this.flag === 0) {
        this.flag = 1
        return
      }
      let areaIndex = picker.getSlotValue(0)
      this.area = areaIndex
    },
    //得到省份数据
    _getProvince() {
      let province = []
      console.log(1)
      areaData.forEach(function(item, index) {
        let obj = {}
        obj.id = item.code
        obj.name = item.name
        province.push(obj)
      })
      return province
    },
    //根据省份得到城市数据
    _getCity(province) {
      let city = []
      console.log(2)
      areaData.forEach((item, index) => {
        if (item.name === province) {
          item.children.forEach((items, index) => {
            let obj = {}
            obj.id = items.code
            obj.name = items.name
            city.push(obj)
            return
          })
        }
      })
      return city
    },
    //根据城市和省份得到区域数据
    _getArea(province, city) {
      let area = []
      areaData.forEach((item, index) => {
        if (item.name === province) {
          item.children.forEach((item, index) => {
            if (item.name === city) {
              item.children.forEach(item => {
                let obj = {}
                obj.id = item.code
                obj.name = item.name
                area.push(obj)
                return
              })
            }
          })
        }
      })
      if (area.length == 0) {
        //如果没有区域数据则第三个picker显示的内容
        area.push({
          name: '没有',
          id: '000000'
        })
      }
      return area
    },
    confirm() {
      let result = {
        name:
          this.province.name + '、' + this.city.name + '、' + this.area.name,
        id: this.province.id + ',' + this.city.id + ',' + this.area.id
      }
      this.$emit('getLinkage', result) //触发父组件的getLinkage事件接收结果数据
    },
    cancel() {
      this.$emit('getLinkage', '')
    }
  }
}
</script>

